-- View: Campaign summary with counts
CREATE OR REPLACE VIEW campaign_summary AS
SELECT
  c.id,
  c.user_id,
  c.target_location,
  c.target_role,
  c.status,
  COUNT(DISTINCT l.id) as leads_count,
  COUNT(DISTINCT ge.id) as emails_count,
  COUNT(DISTINCT es.id) as sends_count,
  SUM(CASE WHEN es.status = 'delivered' THEN 1 ELSE 0 END) as delivered_count,
  SUM(CASE WHEN es.status = 'opened' THEN 1 ELSE 0 END) as opened_count,
  c.created_at,
  c.updated_at
FROM campaigns c
LEFT JOIN leads l ON c.id = l.campaign_id
LEFT JOIN generated_emails ge ON l.id = ge.lead_id
LEFT JOIN email_sends es ON ge.id = es.generated_email_id
GROUP BY c.id, c.user_id, c.target_location, c.target_role, c.status, c.created_at, c.updated_at;

